#include "libft.h"

char	*ft_strdup(char const *src)
{
	char	*dest;
	int		i;
	int		strlen;

	i = 0;
	strlen = ft_strlen(src);
	dest = malloc(sizeof(char) * (strlen + 1));
	if (dest == NULL)
		return (NULL);
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
